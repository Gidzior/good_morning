#!/usr/bin/env node
// Scrape unique holidays ("święta nietypowe") from kalbi.pl for all 366 days.
// Output: frontend/src/lib/holidays.ts (Record<MM-DD, string[]>).
// Run: node scripts/scrape-holidays.mjs

import { writeFile } from 'node:fs/promises';
import { setTimeout as sleep } from 'node:timers/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUTPUT = path.join(__dirname, '..', 'frontend', 'src', 'lib', 'holidays.ts');

const PL_MONTHS = [
  'stycznia', 'lutego', 'marca', 'kwietnia', 'maja', 'czerwca',
  'lipca', 'sierpnia', 'wrzesnia', 'pazdziernika', 'listopada', 'grudnia',
];
const DAYS_IN_MONTH = [31, 29, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];

// Polish month slugs sometimes use diacritic-stripped forms (września → wrzesnia).
// kalbi.pl uses ASCII slugs without diacritics; double-check below if extraction fails.
const RATE_LIMIT_MS = 250;
const USER_AGENT = 'good-morning-dashboard-scraper/1.0 (one-time build)';

function decodeEntities(s) {
  return s
    .replace(/&amp;/g, '&')
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&lt;/g, '<')
    .replace(/&gt;/g, '>')
    .replace(/&nbsp;/g, ' ')
    .replace(/&#(\d+);/g, (_, n) => String.fromCharCode(Number(n)));
}

function extractHolidays(html) {
  // Locate the "Nietypowe święta:" header and capture content until the next </section>.
  const headerIdx = html.indexOf('class="calCard-ententa-t"');
  if (headerIdx === -1) return [];
  const closeIdx = html.indexOf('</section>', headerIdx);
  if (closeIdx === -1) return [];
  const block = html.slice(headerIdx, closeIdx);

  // Each holiday is an <a ... title="X">. Collect titles in order.
  const titles = [];
  const re = /<a\s+[^>]*title="([^"]+)"/gi;
  let m;
  while ((m = re.exec(block)) !== null) {
    const t = decodeEntities(m[1]).trim();
    if (t && !titles.includes(t)) titles.push(t);
  }
  return titles;
}

async function fetchDay(day, monthIdx) {
  const slug = `${day}-${PL_MONTHS[monthIdx]}`;
  const url = `https://www.kalbi.pl/${slug}`;
  const r = await fetch(url, { headers: { 'User-Agent': USER_AGENT } });
  if (!r.ok) throw new Error(`HTTP ${r.status} for ${url}`);
  const html = await r.text();
  return extractHolidays(html);
}

async function main() {
  const result = {};
  let totalEntries = 0;
  let emptyDays = 0;

  for (let m = 0; m < 12; m++) {
    for (let d = 1; d <= DAYS_IN_MONTH[m]; d++) {
      const key = `${String(m + 1).padStart(2, '0')}-${String(d).padStart(2, '0')}`;
      try {
        const titles = await fetchDay(d, m);
        if (titles.length > 0) {
          result[key] = titles;
          totalEntries += titles.length;
        } else {
          emptyDays++;
        }
        process.stdout.write(`\r${key}: ${titles.length} święt(a) (total: ${totalEntries})    `);
      } catch (err) {
        console.error(`\nFailed ${key}: ${err.message}`);
      }
      await sleep(RATE_LIMIT_MS);
    }
  }

  console.log(`\n\nDone. ${Object.keys(result).length} days with holidays, ${emptyDays} empty days, ${totalEntries} total entries.`);

  const lines = [
    '/**',
    ' * Polish unique holidays (święta nietypowe) keyed by MM-DD.',
    ' * Generated by scripts/scrape-holidays.mjs from kalbi.pl.',
    ' * Re-run the script to refresh.',
    ' */',
    'export const WORLD_HOLIDAYS: Record<string, readonly string[]> = {',
  ];
  for (const key of Object.keys(result).sort()) {
    const items = result[key].map(s => JSON.stringify(s)).join(', ');
    lines.push(`  '${key}': [${items}],`);
  }
  lines.push('};');
  lines.push('');
  lines.push('export function getTodayHoliday(date: Date): string | null {');
  lines.push("  const m = String(date.getMonth() + 1).padStart(2, '0');");
  lines.push("  const d = String(date.getDate()).padStart(2, '0');");
  lines.push('  const items = WORLD_HOLIDAYS[`${m}-${d}`];');
  lines.push('  return items?.[0] ?? null;');
  lines.push('}');
  lines.push('');
  lines.push('export function getTodayHolidays(date: Date): readonly string[] {');
  lines.push("  const m = String(date.getMonth() + 1).padStart(2, '0');");
  lines.push("  const d = String(date.getDate()).padStart(2, '0');");
  lines.push('  return WORLD_HOLIDAYS[`${m}-${d}`] ?? [];');
  lines.push('}');
  lines.push('');

  await writeFile(OUTPUT, lines.join('\n'), 'utf8');
  console.log(`Wrote ${OUTPUT}`);
}

main().catch(err => { console.error(err); process.exit(1); });
